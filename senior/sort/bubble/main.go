package main

import "fmt"

/**
冒泡排序
*/

func main() {
	arr := [...]int{10, 32, 93, 12, 4, 23, 54, 25, 23}
	var n = len(arr)
	fmt.Println("-----没排序前\n", arr)
	for i := 0; i <= n-1; i++ {
		fmt.Printf("-----第%d次排序前\n", i+1)
		for j := i; j <= n-1; j++ {
			if arr[i] > arr[j] { // 从小到大排序
				arr[i], arr[j] = arr[j], arr[i]
			}
			fmt.Println(arr)
		}
	}
	fmt.Println("--------最终结果--------\n", arr)
}

